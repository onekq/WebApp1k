import React from 'react';
import { render, screen, act, fireEvent } from '@testing-library/react';
import { MemoryRouter } from 'react-router-dom';
import fetchMock from 'fetch-mock';
import '@testing-library/jest-dom';
import App from './optimizeItinerary_retrieveBookingHistory_removeItemsFromItinerary_validateHotelDetails';

afterEach(() => {
  fetchMock.reset();
  fetchMock.restore();
});

test('successfully optimizes itinerary for travel time and convenience. (from optimizeItinerary_retrieveBookingHistory)', async () => {
  fetchMock.post('/api/optimize-itinerary', { status: 200, body: { success: true } });

  await act(async () => { render(<MemoryRouter><App /></MemoryRouter>); });
  await act(async () => { fireEvent.click(screen.getByTestId('optimize-itinerary-button')); });

  expect(fetchMock.calls('/api/optimize-itinerary', 'POST')).toHaveLength(1);
  expect(screen.getByText('Itinerary optimized')).toBeInTheDocument();
}, 10000);

test('fails to optimize itinerary due to server error. (from optimizeItinerary_retrieveBookingHistory)', async () => {
  fetchMock.post('/api/optimize-itinerary', { status: 500, body: { error: 'Server error' } });

  await act(async () => { render(<MemoryRouter><App /></MemoryRouter>); });
  await act(async () => { fireEvent.click(screen.getByTestId('optimize-itinerary-button')); });

  expect(fetchMock.calls('/api/optimize-itinerary', 'POST')).toHaveLength(1);
  expect(screen.getByText('Server error')).toBeInTheDocument();
}, 10000);

test('Booking history should be retrieved and displayed for valid request. (from optimizeItinerary_retrieveBookingHistory)', async () => {
  fetchMock.get('/api/booking/history', [{ id: 1, status: 'Confirmed' }]);

  await act(async () => { render(<MemoryRouter><App /></MemoryRouter>); });
  await act(async () => { fireEvent.click(screen.getByTestId('get-history')); });

  expect(fetchMock.calls()).toHaveLength(1);
  expect(screen.getByTestId('booking-history')).toBeInTheDocument();
}, 10000);

test('Error in retrieving booking history should show error message. (from optimizeItinerary_retrieveBookingHistory)', async () => {
  fetchMock.get('/api/booking/history', 500);

  await act(async () => { render(<MemoryRouter><App /></MemoryRouter>); });
  await act(async () => { fireEvent.click(screen.getByTestId('get-history')); });

  expect(fetchMock.calls()).toHaveLength(1);
  expect(screen.getByTestId('history-error')).toBeInTheDocument();
}, 10000);

test('successfully removes items from an itinerary. (from removeItemsFromItinerary_validateHotelDetails)', async () => {
  fetchMock.delete('/api/remove-item', { status: 200, body: { success: true } });

  await act(async () => { render(<MemoryRouter><App /></MemoryRouter>); });
  await act(async () => { fireEvent.click(screen.getByTestId('remove-item-button')); });

  expect(fetchMock.calls('/api/remove-item', 'DELETE')).toHaveLength(1);
  expect(screen.queryByTestId('item1')).not.toBeInTheDocument();
}, 10000);

test('fails to remove items due to network error. (from removeItemsFromItinerary_validateHotelDetails)', async () => {
  fetchMock.delete('/api/remove-item', { status: 500, body: { error: 'Network error' } });

  await act(async () => { render(<MemoryRouter><App /></MemoryRouter>); });
  await act(async () => { fireEvent.click(screen.getByTestId('remove-item-button')); });

  expect(fetchMock.calls('/api/remove-item', 'DELETE')).toHaveLength(1);
  expect(screen.getByText('Network error')).toBeInTheDocument();
}, 10000);

test('validateHotelDetails - validates hotel details successfully before booking (from removeItemsFromItinerary_validateHotelDetails)', async () => {
  fetchMock.get('/api/hotels/1/details', {
    body: { id: 1, name: 'Checked Hotel' },
    status: 200,
  });

  await act(async () => {
    render(<MemoryRouter><App /></MemoryRouter>);
  });
  await act(async () => {
    fireEvent.click(screen.getByTestId('validate-hotel-1'));
  });

  expect(fetchMock.calls()).toHaveLength(1);
  expect(screen.getByText('Checked Hotel')).toBeInTheDocument();
}, 10000);

test('validateHotelDetails - shows error when validation fails (from removeItemsFromItinerary_validateHotelDetails)', async () => {
  fetchMock.get('/api/hotels/1/details', {
    body: { message: 'Validation Error' },
    status: 500,
  });

  await act(async () => {
    render(<MemoryRouter><App /></MemoryRouter>);
  });
  await act(async () => {
    fireEvent.click(screen.getByTestId('validate-hotel-1'));
  });

  expect(fetchMock.calls()).toHaveLength(1);
  expect(screen.getByText('Validation Error')).toBeInTheDocument();
}, 10000);

