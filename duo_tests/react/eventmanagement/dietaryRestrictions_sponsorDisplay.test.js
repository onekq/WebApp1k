import React from 'react';
import { render, screen, act, fireEvent } from '@testing-library/react';
import { MemoryRouter } from 'react-router-dom';
import fetchMock from 'fetch-mock';
import '@testing-library/jest-dom';
import App from './dietaryRestrictions_sponsorDisplay';

afterEach(() => {
  fetchMock.reset();
  fetchMock.restore();
});

test('Dietary restrictions are successfully submitted if provided', async () => {
  fetchMock.post('/register-attendee', 200);

  await act(async () => { render(<MemoryRouter><App /></MemoryRouter>); });
  await act(async () => { fireEvent.change(screen.getByLabelText(/dietary restrictions/i), { target: { value: 'Vegetarian' } }); });
  await act(async () => { fireEvent.click(screen.getByText(/submit/i)); });

  expect(fetchMock.calls()).toHaveLength(1);
  expect(screen.getByText(/registration successful/i)).toBeInTheDocument();
}, 10000);

test('Registration fails if dietary restrictions are not provided when required', async () => {
  await act(async () => { render(<MemoryRouter><App /></MemoryRouter>); });
  await act(async () => { fireEvent.click(screen.getByText(/submit/i)); });

  expect(fetchMock.calls()).toHaveLength(0);
  expect(screen.getByText(/please specify dietary restrictions/i)).toBeInTheDocument();
}, 10000);

test('Displays sponsors and partners on event pages', async () => {
  fetchMock.get('/api/event/sponsors', { sponsors: ['Sponsor 1', 'Sponsor 2'], partners: ['Partner 1'] });

  await act(async () => { render(<MemoryRouter><App /></MemoryRouter>); });

  expect(fetchMock.calls().length).toBe(1);
  expect(screen.getByText('Sponsor 1')).toBeInTheDocument();
  expect(screen.getByText('Partner 1')).toBeInTheDocument();
}, 10000);

test('Displays error message when sponsors and partners are unavailable', async () => {
  fetchMock.get('/api/event/sponsors', 404);

  await act(async () => { render(<MemoryRouter><App /></MemoryRouter>); });

  expect(fetchMock.calls().length).toBe(1);
  expect(screen.getByText('Sponsors and partners are unavailable')).toBeInTheDocument();
}, 10000);